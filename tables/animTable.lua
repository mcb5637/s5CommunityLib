--- getRandomAnim   mcb   1.0
-- Gibt eine zufällige animation des entities mit diesem prefix zurück.  
-- 
-- Benötigt:  
-- - GetRandom (mit tableunterstützung)  
-- - animTable  
function getRandomAnim(etyp, name)
	local t = animTable[etyp]
	local ra = {}
	if t[name] then
		table.insert(ra, t[name])
	end
	local i=1
	while t[name..i] do
		table.insert(ra, t[name.i])
		i = i + 1
	end
	return GetRandom(ra)
end
